#addition
def addition(num1 , num2):
    print(num1 + num2)

#subtraction
def subtraction(num1 , num2):
    print(num2 - num1)

#multiplication
def multiplication(num1 , num2):
    print(num1 * num2)

#division
def division(num1 , num2):
    print(num1 / num2)

